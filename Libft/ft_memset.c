#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((unsigned char *)s)[i] = c;
		i++;
	}
	return (s);
}

int main()
{
	int	 s = 5;
	ft_memset(&s, 5, 2);
	ft_memset(&s, 57, 1);
	printf("%d\n", s);

}
